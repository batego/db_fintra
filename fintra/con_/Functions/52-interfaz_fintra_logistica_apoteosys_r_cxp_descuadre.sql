						FIN.CXP_ITEMS_DOC B ON(B.DSTRCT=A.DSTRCT AND B.PROVEEDOR=A.PROVEEDOR AND B.TIPO_DOCUMENTO=A.TIPO_DOCUMENTO AND B.DOCUMENTO=A.DOCUMENTO)
					LEFT JOIN
						PROVEEDOR C ON(C.NIT=B.PROVEEDOR)
					LEFT JOIN
						NIT D ON(D.CEDULA=C.NIT)
					LEFT JOIN
						CIUDAD E ON(E.CODCIU=D.CODCIU)
					LEFT JOIN
						CON.HOMOLOGA_TERCEROS HT ON(HT.NIT_FINTRA=B.PROVEEDOR)
					WHERE
						A.REG_STATUS='' AND
						A.DOCUMENTO in(REC_R4.DOCUMENTO)

				LOOP

				-- AET	-	DT --> TRANSFERENCIA
				-- AGA	-	DG --> GASOLINA
				-- EXT	-	DA --> PRONTOPAGO

					IF(REC_R4.TIPO_DOCUMENTO in('FAP','ND'))THEN
						MCTYPE.MC_____CODIGO____CD_____B := 'AGLG'  ;
						MCTYPE.MC_____DEBMONLOC_B := REC_R5.VLR;
						IF(REC_R4.HANDLE_CODE IN('DT','DG','DA'))THEN
							IF(REC_R4.HANDLE_CODE='DT')THEN
								VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
								_VAR_CU := 'A1111F21401' ;
							ELSIF(REC_R4.HANDLE_CODE='DG')THEN
								VAL_AGA := VAL_AGA+REC_R5.VLR::NUMERIC;
								_VAR_CU := 'A1111F21501';
							ELSIF(REC_R4.HANDLE_CODE='DA')THEN
								VAL_EXT := VAL_EXT+REC_R5.VLR::NUMERIC;
								_VAR_CU := 'A1111F22201';
							END IF;
						ELSE
							IF(REC_R5.CONCEPT_CODE2!='')THEN
								IF(REC_R5.CONCEPT_CODE2='AET')THEN
									VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21401' ;
								ELSIF(REC_R5.CONCEPT_CODE2='AGA')THEN
									VAL_AGA := VAL_AGA+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21501';
								ELSIF(REC_R5.CONCEPT_CODE2='EXT')THEN
