								END IF;
							ELSE
								IF(REC_R5.TIPO='AET')THEN
									VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21401' ;
								ELSIF(REC_R5.TIPO='AGA')THEN
									VAL_AGA := VAL_AGA+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21501';
								ELSIF(REC_R5.TIPO='EXT')THEN
									VAL_EXT := VAL_EXT+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F22201';
								else
									VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21401' ;
								END IF;
							END IF;
						END IF;
						MCTYPE.MC_____DEBMONLOC_B := 0;
					END IF;

					FECHADOC_ := CASE WHEN REPLACE(SUBSTRING(REC_R5.CREATION_DATE,1,7),'-','')=REC_R5.PERIODO THEN REC_R5.CREATION_DATE::DATE ELSE con.sp_fecha_corte_mes(SUBSTRING(REC_R5.PERIODO,1,4),SUBSTRING(REC_R5.PERIODO,5,2)::INT)::DATE END ;

					MCTYPE.MC_____REFERENCI_B := SUBSTR(REC_OS.NUM_INGRESO,3,CHAR_LENGTH(REC_OS.NUM_INGRESO))||';'||REC_OS.CORRIDA  ;
					MCTYPE.MC_____CODIGO____CONTAB_B := 'FINT' ;
					MCTYPE.MC_____CODIGO____TD_____B := 'CXPN' ;
					--MCTYPE.MC_____CODIGO____CD_____B := 'AGLG'  ;
					MCTYPE.MC_____SECUINTE__DCD____B := CONSEC  ;
					MCTYPE.MC_____FECHA_____B := FECHADOC_::DATE  ;
					MCTYPE.MC_____NUMERO____B := SECUENCIA_R  ;
					MCTYPE.MC_____SECUINTE__B := CONSEC  ;
					MCTYPE.MC_____CODIGO____PF_____B := SUBSTRING(REC_R5.PERIODO,1,4)::INT  ;
					MCTYPE.MC_____NUMERO____PERIOD_B := SUBSTRING(REC_R5.PERIODO,5,2)::INT  ;
					MCTYPE.MC_____CODIGO____PC_____B :=  'PUCF' ;
					MCTYPE.MC_____CODIGO____CPC____B := CON.OBTENER_HOMOLOGACION_APOTEOSYS('FUNCIONR', 'FAC', REC_R5.CUENTA,'', 1)  ;
					MCTYPE.MC_____CODIGO____CU_____B := _VAR_CU;
					MCTYPE.MC_____IDENTIFIC_TERCER_B :=  CASE WHEN CHAR_LENGTH(REC_R5.PROVEEDOR)>10 THEN SUBSTR(REC_R5.PROVEEDOR,1,10) ELSE REC_R5.PROVEEDOR END;
					MCTYPE.MC_____DEBMONORI_B := 0  ;
					MCTYPE.MC_____CREMONORI_B := 0 ;
					MCTYPE.MC_____INDTIPMOV_B := 4  ;
					MCTYPE.MC_____INDMOVREV_B := 'N'  ;
					MCTYPE.MC_____OBSERVACI_B := REC_R5.DESCRIPCION||'-'||REC_R4.DOCUMENTO  ;
					MCTYPE.MC_____FECHORCRE_B := REC_R5.CREATION_DATE::TIMESTAMP  ;
					MCTYPE.MC_____AUTOCREA__B := 'ADMIN'  ;
					MCTYPE.MC_____FEHOULMO__B := REC_R5.CREATION_DATE::TIMESTAMP  ;
					MCTYPE.MC_____AUTULTMOD_B := ''  ;
					MCTYPE.MC_____VALIMPCON_B := 0  ;
					MCTYPE.MC_____NUMERO_OPER_B := REC_OS.NUM_INGRESO;
					MCTYPE.TERCER_CODIGO____TIT____B := REC_R5.TERCER_CODIGO____TIT____B  ;
					MCTYPE.TERCER_NOMBCORT__B := SUBSTR(REC_R5.TERCER_NOMBCORT__B,1,32)  ;
					MCTYPE.TERCER_NOMBEXTE__B := SUBSTR(REC_R5.TERCER_NOMBEXTE__B,1,64)  ;
					MCTYPE.TERCER_APELLIDOS_B := SUBSTR(REC_R5.TERCER_APELLIDOS_B,1,32)  ;
					MCTYPE.TERCER_CODIGO____TT_____B := REC_R5.TERCER_CODIGO____TT_____B  ;
					MCTYPE.TERCER_DIRECCION_B := SUBSTR(REC_R5.TERCER_DIRECCION_B,1,64)  ;
					MCTYPE.TERCER_CODIGO____CIUDAD_B := REC_R5.TERCER_CODIGO____CIUDAD_B  ;
					MCTYPE.TERCER_TELEFONO1_B := CASE WHEN CHAR_LENGTH(REC_R5.TERCER_TELEFONO1_B)>15 THEN SUBSTR(REC_R5.TERCER_TELEFONO1_B,1,15) ELSE REC_R5.TERCER_TELEFONO1_B END;
					MCTYPE.TERCER_TIPOGIRO__B := 1 ;
					MCTYPE.TERCER_CODIGO____EF_____B := ''  ;
					MCTYPE.TERCER_SUCURSAL__B := ''  ;
					MCTYPE.TERCER_NUMECUEN__B := ''  ;
					MCTYPE.MC_____CODIGO____DS_____B := '';
					MCTYPE.MC_____NUMDOCSOP_B := '';
					MCTYPE.MC_____NUMEVENC__B := NULL;
					MCTYPE.MC_____FECHEMIS__B='0099-01-01 00:00:00';
					MCTYPE.MC_____FECHVENC__B='0099-01-01 00:00:00';

					--FUNCION QUE TRANSACCION POR TIPO DE DOCUMENTO EN TABLA TEMPORAL EN FINTRA.
					SW:=CON.SP_INSERT_TABLE_MC( MCTYPE);
					CONSEC:=CONSEC+1;
					--RAISE NOTICE 'CONSEC 13: %',CONSEC;

				END LOOP;

				MCTYPE.MC_____CODIGO____DS_____B := 'CXPA';
				MCTYPE.MC_____NUMDOCSOP_B := REC_OS.CORRIDA;
				MCTYPE.MC_____NUMEVENC__B := 1;
				MCTYPE.MC_____OBSERVACI_B := 'DESCUENTO PLANILLA'  ;

				MCTYPE.MC_____FECHEMIS__B=FECHADOC_::DATE;
				MCTYPE.MC_____FECHVENC__B=FECHADOC_::DATE;
				MCTYPE.MC_____CODIGO____CPC____B := '23050103'  ;

				IF(VAL_AET>0)THEN
					-----------------------------------------
					MCTYPE.MC_____SECUINTE__DCD____B := CONSEC  ;
					MCTYPE.MC_____SECUINTE__B := CONSEC  ;
					MCTYPE.MC_____CODIGO____CU_____B := 'A1111F21401';

					IF(REC_R4.TIPO_DOCUMENTO in('FAP','ND'))THEN
						MCTYPE.MC_____CREMONLOC_B := VAL_AET::NUMERIC;
						MCTYPE.MC_____DEBMONLOC_B := 0;
					ELSIF(REC_R4.TIPO_DOCUMENTO='NC')THEN
						MCTYPE.MC_____CREMONLOC_B := 0;
						MCTYPE.MC_____DEBMONLOC_B := VAL_AET::NUMERIC;

					END IF;

					--FUNCION QUE TRANSACCION POR TIPO DE DOCUMENTO EN TABLA TEMPORAL EN FINTRA.
					SW:=CON.SP_INSERT_TABLE_MC( MCTYPE);
					CONSEC:=CONSEC+1;
					-----------------------------------------
				END IF;

				IF(VAL_AGA>0)THEN
					-----------------------------------------
					MCTYPE.MC_____SECUINTE__DCD____B := CONSEC  ;
					MCTYPE.MC_____SECUINTE__B := CONSEC  ;
					MCTYPE.MC_____CODIGO____CU_____B := 'A1111F21501';

					IF(REC_R4.TIPO_DOCUMENTO in('FAP','ND'))THEN
						MCTYPE.MC_____CREMONLOC_B := VAL_AGA::NUMERIC;
						MCTYPE.MC_____DEBMONLOC_B := 0;
					ELSIF(REC_R4.TIPO_DOCUMENTO='NC')THEN
						MCTYPE.MC_____CREMONLOC_B := 0;
						MCTYPE.MC_____DEBMONLOC_B := VAL_AGA::NUMERIC;

					END IF;

					--FUNCION QUE TRANSACCION POR TIPO DE DOCUMENTO EN TABLA TEMPORAL EN FINTRA.
					SW:=CON.SP_INSERT_TABLE_MC( MCTYPE);
					CONSEC:=CONSEC+1;
					-----------------------------------------
				END IF;

				IF(VAL_EXT>0)THEN
					-----------------------------------------
					MCTYPE.MC_____SECUINTE__DCD____B := CONSEC  ;
					MCTYPE.MC_____SECUINTE__B := CONSEC  ;
					MCTYPE.MC_____CODIGO____CU_____B := 'A1111F22201';

					IF(REC_R4.TIPO_DOCUMENTO in('FAP','ND'))THEN
						MCTYPE.MC_____CREMONLOC_B := VAL_EXT::NUMERIC;
						MCTYPE.MC_____DEBMONLOC_B := 0;
					ELSIF(REC_R4.TIPO_DOCUMENTO='NC')THEN
						MCTYPE.MC_____CREMONLOC_B := 0;
						MCTYPE.MC_____DEBMONLOC_B := VAL_EXT::NUMERIC;

					END IF;

					--FUNCION QUE TRANSACCION POR TIPO DE DOCUMENTO EN TABLA TEMPORAL EN FINTRA.
					SW:=CON.SP_INSERT_TABLE_MC( MCTYPE);
					CONSEC:=CONSEC+1;
					-----------------------------------------
				END IF;

				--VALIDAMOS VALORES DEBITOS Y CREDITOS DEL COMPROBANTE A TRASLADAR.
				IF CON.SP_VALIDACIONES(MCTYPE,'LOGISTICA') ='N' THEN
					SW='N';

					--BORRAMOS EL COMPROBANTE DE ING
					IF(REC_R4.TIPO_DOCUMENTO in('FAP','ND'))THEN
						DELETE FROM CON.MC____
						WHERE MC_____NUMERO____B = SECUENCIA_R AND MC_____CODIGO____CONTAB_B = 'FINT'
						 AND MC_____CODIGO____TD_____B = 'CXPN' AND  MC_____CODIGO____CD_____B = 'AGLG'  ;
					ELSIF(REC_R4.TIPO_DOCUMENTO='NC')THEN
						DELETE FROM CON.MC____
						WHERE MC_____NUMERO____B = SECUENCIA_R AND MC_____CODIGO____CONTAB_B = 'FINT'
						 AND MC_____CODIGO____TD_____B = 'CXPN' AND  MC_____CODIGO____CD_____B = 'NCLG'  ;

					END IF;

					CONTINUE;
				END IF;

			END LOOP;

			--------------------------------------------------------------------------------------------------------------------------------------------

			--ACTUALIZAMOS EL REGISTRO EN OS PARA SABER QUE SE PROCESO
			IF(SW='S')THEN
				UPDATE
					EGRESODET_TSP
				SET
					PROCESADO_R='S'
				WHERE
					NUM_INGRESO=REC_OS.NUM_INGRESO;

				SW:='N';
			END IF;
		SW:='N';
	END LOOP;

	RETURN 'OK';

END;
