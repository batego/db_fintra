								END IF;
							ELSE
								IF(REC_R5.TIPO='AET')THEN
									VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21401' ;
								ELSIF(REC_R5.TIPO='AGA')THEN
									VAL_AGA := VAL_AGA+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21501';
								ELSIF(REC_R5.TIPO='EXT')THEN
									VAL_EXT := VAL_EXT+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F22201';
-- 								else
-- 									VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
-- 									_VAR_CU := 'A1111F21401' ;
								END IF;
							END IF;
						END IF;

						MCTYPE.MC_____CREMONLOC_B := 0;
					ELSIF(REC_R4.TIPO_DOCUMENTO='NC')THEN
						MCTYPE.MC_____CODIGO____CD_____B := 'NCLG'  ;
						MCTYPE.MC_____CREMONLOC_B := REC_R5.VLR;
-- 						IF(REC_R4.HANDLE_CODE='DT')THEN
-- 							VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
-- 						ELSIF(REC_R4.HANDLE_CODE='DG')THEN
-- 							VAL_AGA := VAL_AGA+REC_R5.VLR::NUMERIC;
-- 						ELSIF(REC_R4.HANDLE_CODE='DA')THEN
-- 							VAL_EXT := VAL_EXT+REC_R5.VLR::NUMERIC;
-- 						END IF;
						IF(REC_R4.HANDLE_CODE IN('DT','DG','DA'))THEN
							IF(REC_R4.HANDLE_CODE='DT')THEN
								VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
								_VAR_CU := 'A1111F21401' ;
							ELSIF(REC_R4.HANDLE_CODE='DG')THEN
								VAL_AGA := VAL_AGA+REC_R5.VLR::NUMERIC;
								_VAR_CU := 'A1111F21501';
							ELSIF(REC_R4.HANDLE_CODE='DA')THEN
								VAL_EXT := VAL_EXT+REC_R5.VLR::NUMERIC;
								_VAR_CU := 'A1111F22201';
							END IF;
						ELSE
							IF(REC_R5.CONCEPT_CODE2!='')THEN
								IF(REC_R5.CONCEPT_CODE2='AET')THEN
									VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21401' ;
								ELSIF(REC_R5.CONCEPT_CODE2='AGA')THEN
									VAL_AGA := VAL_AGA+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F21501';
								ELSIF(REC_R5.CONCEPT_CODE2='EXT')THEN
									VAL_EXT := VAL_EXT+REC_R5.VLR::NUMERIC;
									_VAR_CU := 'A1111F22201';
-- 								else
-- 									VAL_AET := VAL_AET+REC_R5.VLR::NUMERIC;
