	LOOP

			--CXP DE DESCUADRES
			FOR REC_R4 IN
				SELECT
					DOCUMENTO, TIPO_DOCUMENTO, handle_code
				FROM
					FIN.CXP_DOC
				WHERE
					DOCUMENTO LIKE '%'||REC_OS.NUM_INGRESO||'%'
					AND DOCUMENTO IN('NC_CXPIC306075')
					ORDER BY DOCUMENTO

			LOOP

				--SECUENCIA DE LA R
				SELECT INTO SECUENCIA_R NEXTVAL('CON.INTERFAZ_SECUENCIA_CXP_APOTEOSYS');
				VAL_AET :=0;
				VAL_AGA :=0;
				VAL_EXT :=0;
